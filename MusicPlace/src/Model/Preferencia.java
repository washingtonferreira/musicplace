package Model;

public class Preferencia {
	
	
	private boolean rock;
	private boolean samba;
	private boolean axe;
	private boolean pagode;
	private boolean forro;
	private boolean erudita;
	private boolean mpb;
	private boolean jazz;
	
	
	public boolean isRock() {
		return rock;
	}
	
	public void setRock(boolean rock) {
		this.rock = rock;
	}
	
	public boolean isSamba() {
		return samba;
	}
	
	public void setSamba(boolean samba) {
		this.samba = samba;
	}
	
	public boolean isAxe() {
		return axe;
	}
	
	public void setAxe(boolean axe) {
		this.axe = axe;
	}
	
	public boolean isPagode() {
		return pagode;
	}
	
	public void setPagode(boolean pagode) {
		this.pagode = pagode;
	}
	
	public boolean isForro() {
		return forro;
	}
	
	public void setForro(boolean forro) {
		this.forro = forro;
	}
	
	public boolean isErudita() {
		return erudita;
	}
	
	public void setErudita(boolean erudita) {
		this.erudita = erudita;
	}
	
	public boolean isMpb() {
		return mpb;
	}
	
	public void setMpb(boolean mpb) {
		this.mpb = mpb;
	}
	
	public boolean isJazz() {
		return jazz;
	}
	
	public void setJazz(boolean jazz) {
		this.jazz = jazz;
	}
	

}
