package Model;

public class Musica {

	private String nomeMus;
	private String nomeAlb;
	private String tempoMus;
	private String linkMus;
	private String linkclipe;
	
	
	public String getNomeMus() {
		return nomeMus;
	}
	
	public void setNomeMus(String nomeMus) {
		this.nomeMus = nomeMus;
	}
	
	public String getNomeAlb() {
		return nomeAlb;
	}
	
	public void setNomeAlb(String nomeAlb) {
		this.nomeAlb = nomeAlb;
	}
	
	public String getTempoMus() {
		return tempoMus;
	}
	
	public void setTempoMus(String tempoMus) {
		this.tempoMus = tempoMus;
	}
	
	public String getLinkMus() {
		return linkMus;
	}
	
	public void setLinkMus(String linkMus) {
		this.linkMus = linkMus;
	}
	
	public String getLinkclipe() {
		return linkclipe;
	}
	
	public void setLinkclipe(String linkclipe) {
		this.linkclipe = linkclipe;
	}
			
}
